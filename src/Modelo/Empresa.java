package Modelo;

public class Empresa {

    private String nombre;
    private String fecha_inic_act;

    public Empresa() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFecha_inic_act() {
        return fecha_inic_act;
    }

    public void setFecha_inic_act(String fecha_inic_act) {
        this.fecha_inic_act = fecha_inic_act;
    }

}
